CScene* CScene::Create(MODE mode)
{
	CScene* pScene = nullptr;

	switch (mode)
	{
	case MODE::MODE_TITLE:
		pScene = new CTitle;

		break;

	case MODE::MODE_GAME:
		pScene = new CGame;

		break;

	case MODE::MODE_TUTORIAL:
		pScene = new CTutorial;

		break;

	case MODE::MODE_RESULT:
		pScene = new CResult;

		break;
	}

	m_Mode = mode;

	return pScene;
}

CScene::MODE CScene::GetMode()
{
	return m_Mode;
}